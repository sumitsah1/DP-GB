def laplace_noise(scale, size):
    return np.random.laplace(loc=0, scale=scale, size=size)

def dp_mean(data, epsilon):
    """
    Differentially private mean of data
    Corrected: sensitivity = sqrt(d) / n
    """
    n, d = data.shape
    sensitivity = np.sqrt(d) / n   # L2 sensitivity of the mean
    noise = laplace_noise(scale=sensitivity/epsilon, size=d)
    return np.mean(data, axis=0) + noise

def dp_radius(data, center, epsilon, bounds=(0,1)):
    """
    Differentially private radius using exact global sensitivity.
    Assumes all features are within [bounds[0], bounds[1]].
    """
    distances = np.linalg.norm(data - center, axis=1)
    true_radius = np.max(distances)
    # Global sensitivity of max distance to a fixed point in bounded domain
    diameter = np.sqrt(data.shape[1]) * (bounds[1] - bounds[0])
    sensitivity = diameter
    noise = np.random.laplace(loc=0, scale=sensitivity/epsilon)
    return max(0, true_radius + noise)

def dp_radius_quantile(data, center, epsilon, q=0.95, sensitivity=None):
    """
    Use the exponential mechanism to select a radius that covers at least q fraction of points.
    This is more stable than the max.
    """
    distances = np.linalg.norm(data - center, axis=1)
    # Sort distances
    sorted_dists = np.sort(distances)
    n = len(sorted_dists)
    # Utility scores: for each possible radius (choose a distance from the sorted list)
    # Score = number of points within that radius, clipped to ensure DP.
    # We use the exponential mechanism.
    scores = np.array([min(np.sum(distances <= r), n) for r in sorted_dists])
    # Sensitivity of the score function is 1 (adding one point changes count by at most 1)
    # Apply exponential mechanism: probability proportional to exp(epsilon * score / (2 * sensitivity))
    probabilities = np.exp(epsilon * scores / (2 * 1))
    probabilities /= probabilities.sum()
    chosen_idx = np.random.choice(len(sorted_dists), p=probabilities)
    return sorted_dists[chosen_idx]

