    alphas = [(a, b, c) for a in [0.2, 0.3, 0.4]
                         for b in [0.3, 0.4, 0.5]
                         for c in [0.1, 0.15, 0.2]
                         if a + b + c <= 1.0]

    best_acc = 0
    best_split = (0.3, 0.4, 0.15)

