# ---- DP-Naive Bayes (Vaidya et al. 2013 style) ----
def dp_naive_bayes_train(X, y, epsilon):
    """
    Differentially private Gaussian Naive Bayes.
    Adds Laplace noise to per-class mean and variance.
    Privacy budget split: epsilon/2 for mean, epsilon/2 for variance.
    Satisfies epsilon-DP by sequential composition.
    """
    classes = np.unique(y)
    n, d = X.shape
    eps_mean = epsilon / 2
    eps_var  = epsilon / 2

    params = {}
    for c in classes:
        X_c = X[y == c]
        n_c = len(X_c)
        # DP mean (sensitivity = sqrt(d)/n_c for [0,1]^d)
        sens_mean = np.sqrt(d) / n_c
        dp_mu = np.mean(X_c, axis=0) + np.random.laplace(0, sens_mean/eps_mean, size=d)
        # DP variance (sensitivity = 1/n_c for [0,1] bounded)
        sens_var = 1.0 / n_c
        dp_var = np.var(X_c, axis=0) + np.random.laplace(0, sens_var/eps_var, size=d)
        dp_var = np.clip(dp_var, 1e-6, None)
        params[c] = {'mean': dp_mu, 'var': dp_var, 'prior': n_c / n}
    return params

def dp_naive_bayes_predict(X_test, params):
    """Predict using DP-NB log-posterior."""
    y_pred = []
    for x in X_test:
        log_posts = {}
        for c, p in params.items():
            # Log Gaussian likelihood per feature
            log_lik = -0.5 * np.sum(np.log(2 * np.pi * p['var']) +
                                     (x - p['mean'])**2 / p['var'])
            log_posts[c] = np.log(p['prior'] + 1e-10) + log_lik
        y_pred.append(max(log_posts, key=log_posts.get))
    return np.array(y_pred)

# ---- DP-kNN (local DP style) ----
def dp_knn_predict(X_train, y_train, X_test, k=5, epsilon=1.0):
    """
    Differentially private k-NN via randomized response on labels.
    Each training label is flipped with probability p = 1/(1 + exp(epsilon_local))
    This satisfies epsilon_local-LDP per point.
    Note: We use epsilon as the LOCAL DP parameter for the label release.
    """
    n_classes = len(np.unique(y_train))
    # Flip probability under LDP (randomized response)
    p_keep = np.exp(epsilon) / (np.exp(epsilon) + n_classes - 1)

    # Privatize labels
    privatized_labels = []
    for label in y_train:
        if np.random.random() < p_keep:
            privatized_labels.append(label)
        else:
            # Randomly pick from other classes
            others = [c for c in np.unique(y_train) if c != label]
            privatized_labels.append(np.random.choice(others))
    privatized_labels = np.array(privatized_labels)

    # Predict: find k nearest neighbors, majority vote on privatized labels
    y_pred = []
    for x in X_test:
        dists = np.linalg.norm(X_train - x, axis=1)
        nn_idx = np.argsort(dists)[:k]
        nn_labels = privatized_labels[nn_idx]
        y_pred.append(np.bincount(nn_labels.astype(int)).argmax())
    return np.array(y_pred)

