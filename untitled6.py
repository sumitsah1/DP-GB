from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier

def membership_inference_shadow_enhanced(X, y, build_score_func, n_shadow=30, test_size=0.3):
    """
    Shadow model membership inference attack using confidence scores.

    Parameters
    ----------
    X, y : full dataset
    build_score_func : function that takes (X, y) and returns a function that, given X_new,
                       returns a matrix of scores (e.g., distances to centers) of shape (n_samples, n_balls).
    n_shadow : number of shadow models
    test_size : fraction of target data used for test (non-members)

    Returns
    -------
    attack_acc : float, accuracy of the attack classifier.
    """
    # Split full data into target and shadow pools
    X_target, X_shadow, y_target, y_shadow = train_test_split(
        X, y, test_size=0.5, random_state=42, stratify=y
    )
    # Split target into train (members) and test (non-members)
    X_target_train, X_target_test, y_target_train, y_target_test = train_test_split(
        X_target, y_target, test_size=test_size, random_state=42, stratify=y_target
    )

    attack_features = []
    attack_labels = []

    for i in range(n_shadow):
        # Sample shadow train/test
        idx_train, idx_test = train_test_split(
            np.arange(len(X_shadow)), test_size=0.5, random_state=i, stratify=y_shadow
        )
        X_shadow_train = X_shadow[idx_train]
        y_shadow_train = y_shadow[idx_train]
        X_shadow_test  = X_shadow[idx_test]
        y_shadow_test  = y_shadow[idx_test]

        # Build shadow score function
        shadow_score_func = build_score_func(X_shadow_train, y_shadow_train)

        # Get scores for members
        scores_train = shadow_score_func(X_shadow_train)
        # Determine predicted class (closest center)
        pred_train = np.argmin(scores_train, axis=1)
        for true, pred, score_vec in zip(y_shadow_train, pred_train, scores_train):
            # Feature: confidence (score for predicted class) + correctness
            confidence = score_vec[pred]  # e.g., distance to closest center
            attack_features.append([confidence, 1 if pred == true else 0])
            attack_labels.append(1)  # member

        # Get scores for non-members
        scores_test = shadow_score_func(X_shadow_test)
        pred_test = np.argmin(scores_test, axis=1)
        for true, pred, score_vec in zip(y_shadow_test, pred_test, scores_test):
            confidence = score_vec[pred]
            attack_features.append([confidence, 1 if pred == true else 0])
            attack_labels.append(0)  # non-member

    # Train attack classifier
    attack_clf = RandomForestClassifier(n_estimators=100, random_state=42)
    attack_clf.fit(attack_features, attack_labels)

    # Build target model
    target_score_func = build_score_func(X_target_train, y_target_train)

    # Get scores on target members and non-members
    scores_train = target_score_func(X_target_train)
    scores_test  = target_score_func(X_target_test)

    pred_train = np.argmin(scores_train, axis=1)
    pred_test  = np.argmin(scores_test, axis=1)

    feat_train = [[score_vec[pred], 1 if pred == true else 0]
                  for score_vec, pred, true in zip(scores_train, pred_train, y_target_train)]
    feat_test  = [[score_vec[pred], 1 if pred == true else 0]
                  for score_vec, pred, true in zip(scores_test, pred_test, y_target_test)]

    attack_pred_train = attack_clf.predict(feat_train)
    attack_pred_test  = attack_clf.predict(feat_test)

    true_all = [1]*len(attack_pred_train) + [0]*len(attack_pred_test)
    pred_all = np.concatenate([attack_pred_train, attack_pred_test])
    acc = np.mean(pred_all == true_all)
    return acc

