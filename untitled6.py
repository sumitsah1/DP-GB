def predict_class(X_test, balls):
    y_pred = []
    for x in X_test:
        distances = [np.linalg.norm(x - balls[c]['center']) for c in balls]
        y_pred.append(list(balls.keys())[np.argmin(distances)])
    return np.array(y_pred)
def predict_multi_ball(X_test, balls):
    """
    Predict class for each test point using nearest DP ball center.
    If inside at least one ball, use the class of the nearest center.
    Otherwise (outside all), use class of nearest center.
    """
    y_pred = []
    for x in X_test:
        # Find nearest ball center
        best_dist = float('inf')
        best_class = None
        inside_any = False
        for ball in balls:
            dist = np.linalg.norm(x - ball['center'])
            if dist <= ball['radius']:
                inside_any = True
                if dist < best_dist:
                    best_dist = dist
                    best_class = ball['class']
            else:
                # Only consider as candidate if not inside any ball yet
                if not inside_any and dist < best_dist:
                    best_dist = dist
                    best_class = ball['class']
        y_pred.append(best_class)
    return np.array(y_pred)

