def dp_granular_balls(X, y, epsilon_center, epsilon_radius):
    """
    Build granular balls with DP protected center and DP protected radius
    epsilon_center and epsilon_radius split privacy budget
    """
    classes = np.unique(y)
    balls = {}
    for c in classes:
        pts = X[y == c]
        dp_c = dp_mean(pts, epsilon_center)
        dp_r = dp_radius(pts, dp_c, epsilon_radius)
        balls[c] = {'center': dp_c, 'radius': dp_r}
    return balls

