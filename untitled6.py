def find_optimal_budget_split(X_train, X_test, y_train, y_test,
                               epsilon=1.0, n_trials=20, n_repeats=10):
    """
    Empirically search for the best (eps_center, eps_radius, eps_split)
    split for a given dataset and epsilon.
    Returns the best split and its accuracy.
    """
