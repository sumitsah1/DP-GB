def optimal_budget_split_theory(epsilon_total, n, d):
    """
    Proposition 2 (Optimal Budget Split):
    For fixed total epsilon, the split that minimizes combined noise variance is:
        epsilon_center* = epsilon_total * (w_c / (w_c + w_r))
        epsilon_radius* = epsilon_total * (w_r / (w_c + w_r))
    where w_c = noise_var_center_weight, w_r = noise_var_radius_weight.

    Noise variance for Laplace(scale=s) = 2*s^2
    For center: noise_var per dim = 2*(sqrt(d)/(n*eps_c))^2 = 2d/(n^2 * eps_c^2)
    For radius: noise_var = 2*(sqrt(d)/eps_r)^2 = 2d/eps_r^2

    Setting partial derivatives equal -> optimal split is NOT 50/50.
    For large n, most budget should go to radius (center noise shrinks with n).
    """
    # Grid search for empirical optimal split
    best_split = None
    best_combined_var = float('inf')
    for alpha in np.linspace(0.05, 0.95, 100):
        eps_c = alpha * epsilon_total
        eps_r = (1 - alpha) * epsilon_total
        var_center = 2 * d / (n**2 * eps_c**2)   # per-dimension, summed
        var_radius = 2 * d / (eps_r**2)
        combined = var_center + var_radius
        if combined < best_combined_var:
            best_combined_var = combined
            best_split = alpha
    print(f"\n[Proposition 2 — Optimal Budget Split]")
    print(f"  Total epsilon={epsilon_total}, n={n}, d={d}")
    print(f"  Optimal fraction for center: alpha* = {best_split:.3f}")
    print(f"  => epsilon_center* = {best_split*epsilon_total:.4f}")
    print(f"  => epsilon_radius* = {(1-best_split)*epsilon_total:.4f}")
    print(f"  (Note: naive 50/50 split is suboptimal for large n)")
    return best_split
