def purity(y_subset):
    """Fraction of majority class in a node."""
    if len(y_subset) == 0:
        return 1.0
    vals, counts = np.unique(y_subset, return_counts=True)
    return counts.max() / len(y_subset)

def dp_split_ball(X_sub, epsilon_split):
    """
    DP-private split: find the feature with highest DP variance
    and split at the DP-private median.
    Satisfies (2*epsilon_split)-DP by composition.
    """
    n, d = X_sub.shape
    # DP variance per feature (Laplace noise on variance)
    variances = np.var(X_sub, axis=0)
    sensitivity_var = 1.0 / n   # sensitivity of variance in [0,1]^d
    noisy_var = variances + np.random.laplace(0, sensitivity_var / epsilon_split, size=d)
    best_feat = np.argmax(noisy_var)

    # DP median via Laplace noise on true median
    true_median = np.median(X_sub[:, best_feat])
    sensitivity_median = 1.0 / n
    dp_med = true_median + np.random.laplace(0, sensitivity_median / epsilon_split)
    dp_med = np.clip(dp_med, 0, 1)
    return best_feat, dp_med

def dp_multi_ball_recursive(X_sub, y_sub, epsilon_center, epsilon_radius,
                             epsilon_split, max_depth=3, min_samples=5,
                             purity_threshold=0.7, depth=0):
    """
    Recursively build multiple DP granular balls per class.
    Now with debug prints and relaxed stopping.
    """
    balls = []
    n, d = X_sub.shape

    # Stopping conditions (relaxed)
    if (n < min_samples or
        depth >= max_depth or
        purity(y_sub) >= purity_threshold):
        if n == 0:
            return balls
        # Build a single DP ball for this leaf
        dp_c = dp_mean(X_sub, epsilon_center)
        dp_r = dp_radius_quantile(X_sub, dp_c, epsilon_radius)
        majority_class = np.bincount(y_sub.astype(int)).argmax()
        ball = {
            'center': dp_c,
            'radius': max(dp_r, 1e-6),
            'class': majority_class,
            'n_points': n,
            'depth': depth,
            'purity': purity(y_sub)
        }
        balls.append(ball)
        print(f"  [depth={depth}] Created ball with {n} points, purity={purity(y_sub):.2f}")
        return balls

    # DP Split
    feat, threshold = dp_split_ball(X_sub, epsilon_split)
    left_mask = X_sub[:, feat] <= threshold
    right_mask = ~left_mask

    X_left,  y_left  = X_sub[left_mask],  y_sub[left_mask]
    X_right, y_right = X_sub[right_mask], y_sub[right_mask]

    print(f"  [depth={depth}] Split on feature {feat} at {threshold:.3f}: left={len(X_left)}, right={len(X_right)}")

    # Recurse on both halves
    balls_left  = dp_multi_ball_recursive(X_left, y_left, epsilon_center, epsilon_radius,
                                           epsilon_split, max_depth, min_samples,
                                           purity_threshold, depth+1)
    balls_right = dp_multi_ball_recursive(X_right, y_right, epsilon_center, epsilon_radius,
                                           epsilon_split, max_depth, min_samples,
                                           purity_threshold, depth+1)
    balls.extend(balls_left)
    balls.extend(balls_right)

    # If recursion produced nothing (both halves empty? unlikely), fallback
    if len(balls) == 0:
        dp_c = dp_mean(X_sub, epsilon_center)
        dp_r = dp_radius_quantile(X_sub, dp_c, epsilon_radius)
        majority_class = np.bincount(y_sub.astype(int)).argmax()
        balls.append({
            'center': dp_c, 'radius': max(dp_r, 1e-6),
            'class': majority_class, 'n_points': n,
            'depth': depth, 'purity': purity(y_sub)
        })
    return balls

def dp_granular_balls_multi(X, y, epsilon_total, budget_split=(0.3, 0.4, 0.15),
                             max_depth=3, min_samples=10, purity_threshold=0.9):
    """
    Full Multi-Ball DP Granular Ball Classifier.

    Budget split (must sum <= 1.0):
      budget_split[0] -> fraction for center  (epsilon_center)
      budget_split[1] -> fraction for radius  (epsilon_radius)
      budget_split[2] -> fraction for split   (epsilon_split)

    Total privacy cost (by parallel + sequential composition):
      epsilon_total (exact, since each sample touches only one leaf ball)
    """
    assert sum(budget_split) <= 1.0, "Budget split must sum to <= 1.0"
    eps_c = epsilon_total * budget_split[0]
    eps_r = epsilon_total * budget_split[1]
    eps_s = epsilon_total * budget_split[2]

    all_balls = []
    classes = np.unique(y)
    for c in classes:
        mask = (y == c)
        X_c, y_c = X[mask], y[mask]
        class_balls = dp_multi_ball_recursive(
            X_c, y_c, eps_c, eps_r, eps_s,
            max_depth=max_depth,
            min_samples=min_samples,
            purity_threshold=purity_threshold
        )
        all_balls.extend(class_balls)
    return all_balls

